package Encapsulation_14;

public class Encapsulation1  {
	protected String name;
	protected int rno;

	public String getName() {
		return name;
	}

	public void setName(String name) {
        this.name = name;
	}

	public int getRno() {
		return rno;
	}

	public void setRno(int rno) {
		this.rno = rno;
	}
}
